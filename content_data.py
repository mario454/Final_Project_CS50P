# Convert dict to sorted list of list to make table
def convert_to_list_of_list(dict_):
    list_of_list = []
    for model, price in sorted(dict_.items(), key= lambda item:(item[1], item[0])):
        list_of_list.append([model, price])
    return list_of_list

